const Player = function(color,x=64,y=64) {
	this.color = color;
	this.x = x;
	this.y = y;
	this.width = 16;
	this.height = 16;
	this.velocity_x = 0;
	this.velocity_y = 0;

	this.move = function(speed, mod, angle) {
		this.x += Math.cos(angle) * speed * 1/mod;
		this.y += Math.sin(angle) * speed * 1/mod;
	}

}
